#class pyrogram.types.ChatPhoto
#https://docs.pyrogram.org/api/types/ChatPhoto#pyrogram.types.ChatPhoto

message.chat.photo.small_file_id        # ( str) - Идентификатор файла маленькой (160x160) фотографии чата. Этот file_id можно использовать только для загрузки фотографий и только до тех пор, пока фотография не будет изменена.
message.chat.photo.big_file_id          # ( str) - Идентификатор файла большой (640x640) фотографии чата. Этот file_id можно использовать только для загрузки фотографий и только до тех пор, пока фотография не будет изменена.
